// ---- 言語の保存/取得 ----
export function getLang() {
  try { return localStorage.getItem("jpVocab.lang") || "en"; } catch {}
  return "en";
}
function applyLangToHtml(code) {
  try { document.documentElement.setAttribute("lang", code); } catch {}
}
export function setLang(code) {
  try { localStorage.setItem("jpVocab.lang", code); } catch {}
  applyLangToHtml(code);
}
// 初期読込時にも現在値を反映（←ここが大事）
applyLangToHtml(getLang());
// ---- 翻訳関数 ----
export function t(key, vars = {}) {
  const lang = getLang();
  const dict = MESSAGES[lang] || MESSAGES.en || {};
  let s = dict[key] ?? (MESSAGES.en ? MESSAGES.en[key] : undefined) ?? key;
  // {n} などの置換
  s = s.replace(/\{(\w+)\}/g, (_, k) => (vars[k] ?? ""));
  return s;
}


